    }

    public function save_all_as_png(){
        $actions = \App\Action::all();
        foreach($actions as $action){
            echo "Saving " . $action->name . "...<br>";
            $status = $action->save_as_png();
            print_r($status);
        }
        return "<br>Done<br>";
    }

